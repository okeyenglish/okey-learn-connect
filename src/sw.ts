// Type for Workbox manifest entries
type ManifestEntry = PrecacheEntry | string;

// Precache all assets generated by vite-plugin-pwa
// On some Safari/iOS setups (or if a stale SW is served), __WB_MANIFEST may be missing.
// Workbox will throw at evaluation time which prevents SW registration entirely.
// Fallback to an empty manifest so push can still work.
const wbManifest = (self as unknown as { __WB_MANIFEST?: ManifestEntry[] }).__WB_MANIFEST;
precacheAndRoute(Array.isArray(wbManifest) ? wbManifest : []);

// Clean up old caches
cleanupOutdatedCaches();

// ============= CACHE NAMES =============
const API_CACHE_NAME = 'api-cache-v1';
const STATIC_CACHE_NAME = 'static-cache-v1';
const IMAGE_CACHE_NAME = 'chat-images-v1';

// Cache expiration: 24 hours for API, 7 days for static
const API_CACHE_MAX_AGE = 24 * 60 * 60 * 1000; // 24 hours
const STATIC_CACHE_MAX_AGE = 7 * 24 * 60 * 60 * 1000; // 7 days

// API endpoints to cache with stale-while-revalidate
const API_PATTERNS = [
  /\/rest\/v1\/organizations/,
  /\/rest\/v1\/profiles/,
  /\/rest\/v1\/teachers/,
  /\/rest\/v1\/learning_groups/,
  /\/rest\/v1\/messenger_settings/,
  /\/functions\/v1\/get-organization-data/,
  /\/functions\/v1\/get-branches/,
];

// Check if URL matches any API pattern
function isApiRequest(url: URL): boolean {
  return API_PATTERNS.some(pattern => pattern.test(url.pathname));
}

// Check if URL is a Supabase request (for API caching)
function isSupabaseRequest(url: URL): boolean {
  return url.hostname.includes('supabase') || 
         url.hostname.includes('academyos.ru') ||
         url.pathname.includes('/rest/v1/') ||
         url.pathname.includes('/functions/v1/');
}

// Check if request is cacheable (GET only, no auth-sensitive)
function isCacheableRequest(request: Request, url: URL): boolean {
  if (request.method !== 'GET') return false;
  
  // Skip auth endpoints
  if (url.pathname.includes('/auth/')) return false;
  
  // Skip realtime
  if (url.pathname.includes('/realtime/')) return false;
  
  return true;
}

// Clean expired entries from cache
async function cleanExpiredCache(cacheName: string, maxAge: number): Promise<void> {
  try {
    const cache = await caches.open(cacheName);
    const requests = await cache.keys();
    const now = Date.now();
    
    for (const request of requests) {
      const response = await cache.match(request);
      if (response) {
        const cachedTime = response.headers.get('sw-cached-at');
        if (cachedTime && (now - parseInt(cachedTime, 10)) > maxAge) {
          await cache.delete(request);
        }
      }
    }
  } catch (e) {
    console.warn('[SW] Failed to clean cache:', e);
  }
}

// Add timestamp header to response for expiration tracking
function addCacheHeaders(response: Response): Response {
  const headers = new Headers(response.headers);
  headers.set('sw-cached-at', Date.now().toString());
  return new Response(response.body, {
    status: response.status,
    statusText: response.statusText,
    headers,
  });
}

// ============= STALE-WHILE-REVALIDATE FOR API =============
// Strategy: Return cached response immediately, then update cache in background

async function staleWhileRevalidate(request: Request, cacheName: string): Promise<Response> {
  const cache = await caches.open(cacheName);
  const cachedResponse = await cache.match(request);
  
  // Start network request in background
  const networkPromise = fetch(request).then(async (networkResponse) => {
    if (networkResponse.ok) {
      // Clone and add cache timestamp
      const responseToCache = addCacheHeaders(networkResponse.clone());
      await cache.put(request, responseToCache);
      console.log('[SW] API cache updated:', request.url.substring(0, 60));
    }
    return networkResponse;
  }).catch((error) => {
    console.warn('[SW] Network request failed:', request.url.substring(0, 60), error);
    return null;
  });

  // If we have cached response, return it immediately
  if (cachedResponse) {
    console.log('[SW] API cache hit (stale-while-revalidate):', request.url.substring(0, 60));
    // Don't await - let it update in background
    networkPromise.catch(() => {});
    return cachedResponse;
  }

  // No cache, wait for network
  const networkResponse = await networkPromise;
  if (networkResponse) {
    return networkResponse;
  }

  // Both cache and network failed
  return new Response(JSON.stringify({ error: 'Offline', cached: false }), {
    status: 503,
    statusText: 'Service Unavailable',
    headers: { 'Content-Type': 'application/json' },
  });
}

// ============= NETWORK-FIRST FOR STATIC ASSETS =============
async function networkFirst(request: Request, cacheName: string): Promise<Response> {
  try {
    const networkResponse = await fetch(request);
    if (networkResponse.ok) {
      const cache = await caches.open(cacheName);
      const responseToCache = addCacheHeaders(networkResponse.clone());
      await cache.put(request, responseToCache);
    }
    return networkResponse;
  } catch (error) {
    const cache = await caches.open(cacheName);
    const cachedResponse = await cache.match(request);
    if (cachedResponse) {
      console.log('[SW] Static cache hit (offline):', request.url.substring(0, 60));
      return cachedResponse;
    }
    throw error;
  }
}

// ============= CACHE-FIRST FOR IMAGES =============
async function cacheFirstImages(request: Request): Promise<Response> {
  const cache = await caches.open(IMAGE_CACHE_NAME);
  const cachedResponse = await cache.match(request);
  
  if (cachedResponse) {
    console.log('[SW] Image cache hit:', request.url.substring(0, 50));
    return cachedResponse;
  }

  try {
    const networkResponse = await fetch(request);
    if (networkResponse.ok) {
      cache.put(request, networkResponse.clone());
      console.log('[SW] Image cached from network:', request.url.substring(0, 50));
    }
    return networkResponse;
  } catch (error) {
    console.warn('[SW] Image fetch failed:', request.url.substring(0, 50), error);
    return new Response('Image not available offline', {
      status: 503,
      statusText: 'Service Unavailable',
    });
  }
}

// ============= MAIN FETCH HANDLER =============
self.addEventListener('fetch', (event) => {
  const url = new URL(event.request.url);
  const request = event.request;

  // Skip non-GET requests
  if (request.method !== 'GET') return;

  // Skip chrome-extension, etc.
  if (!url.protocol.startsWith('http')) return;

  // 1. Handle API requests with stale-while-revalidate
  if (isSupabaseRequest(url) && isApiRequest(url) && isCacheableRequest(request, url)) {
    event.respondWith(staleWhileRevalidate(request, API_CACHE_NAME));
    return;
  }

  // 2. Handle images (external) with cache-first
  const isImage =
    request.destination === 'image' ||
    /\.(jpg|jpeg|png|gif|webp|svg|bmp)(\?.*)?$/i.test(url.pathname);

  if (isImage && url.origin !== self.location.origin) {
    event.respondWith(cacheFirstImages(request));
    return;
  }

  // 3. Handle static assets (fonts, external scripts) with network-first
  const isStaticAsset =
    request.destination === 'font' ||
    /\.(woff2?|ttf|otf)(\?.*)?$/i.test(url.pathname);

  if (isStaticAsset && url.origin !== self.location.origin) {
    event.respondWith(networkFirst(request, STATIC_CACHE_NAME));
    return;
  }

  // Let other requests pass through to Workbox precache or network
});

// ============= PUSH NOTIFICATIONS =============

// Types for push notification data
interface PushNotificationData {
  title?: string;
  body?: string;
  icon?: string;
  badge?: string;
  tag?: string;
  url?: string;
  requireInteraction?: boolean;
  data?: Record<string, unknown>;
  notification?: PushNotificationData; // FCM-like nested format
}

self.addEventListener('push', (event) => {
  const rawText = event.data?.text() || '';
  const timestamp = new Date().toISOString();
  
  console.log('[SW] Push received at:', timestamp, 'raw:', rawText.substring(0, 200));
  
  // Store raw payload for diagnostics (keep last 5)
  try {
    const diagnostics = JSON.parse(self.indexedDB ? '[]' : '[]');
    // Use postMessage to store in main thread since SW can't access localStorage directly
    self.clients.matchAll({ type: 'window', includeUncontrolled: true }).then(clients => {
      clients.forEach(client => {
        client.postMessage({
          type: 'PUSH_RAW_PAYLOAD',
          payload: rawText,
          receivedAt: timestamp,
        });
      });
    });
  } catch (e) {
    console.warn('[SW] Failed to store diagnostic:', e);
  }
  
  const defaultData: PushNotificationData = {
    title: "O'KEY ENGLISH",
    body: 'Новое уведомление',
    icon: '/pwa-192x192.png',
    badge: '/pwa-192x192.png',
    tag: 'default',
    data: {}
  };

  let data: PushNotificationData = { ...defaultData };

  try {
    if (event.data) {
      const payload = event.data.json() as PushNotificationData;
      console.log('[SW] Parsed payload:', JSON.stringify(payload).substring(0, 200));
      
      // Support both flat format and nested notification format (FCM-like)
      if (payload.notification && typeof payload.notification === 'object') {
        const notif = payload.notification as PushNotificationData;
        data = { ...defaultData, ...notif, data: payload.data || notif.data };
      } else {
        data = { ...defaultData, ...payload };
      }
    }
  } catch (e) {
    console.error('[SW] Error parsing push data:', e, 'raw:', rawText);
    // Try as text
    if (event.data) {
      data.body = event.data.text();
    }
  }

  // Extended notification options for Service Worker (includes vibrate which is not in standard NotificationOptions)
  interface ExtendedNotificationOptions extends NotificationOptions {
    vibrate?: number[];
    actions?: Array<{ action: string; title: string; icon?: string }>;
  }

  const options: ExtendedNotificationOptions = {
    body: data.body,
    icon: data.icon || '/pwa-192x192.png',
    badge: data.badge || '/pwa-192x192.png',
    tag: data.tag || 'okey-notification',
    vibrate: [100, 50, 100],
    data: {
      ...data.data,
      url: data.url || data.data?.url || '/',
      dateOfArrival: Date.now(),
    },
    actions: [
      { action: 'open', title: 'Открыть' },
      { action: 'close', title: 'Закрыть' }
    ],
    requireInteraction: data.requireInteraction || false,
  };

  const broadcastToClients = async () => {
    try {
      const clientList = await self.clients.matchAll({ type: 'window', includeUncontrolled: true });
      for (const client of clientList) {
        client.postMessage({
          type: 'PUSH_RECEIVED',
          payload: {
            title: data.title,
            body: data.body,
            tag: data.tag,
            url: (options.data as Record<string, unknown> | undefined)?.url,
          },
          receivedAt: Date.now(),
        });
      }
    } catch (e) {
      console.warn('[SW] Failed to broadcast push to clients:', e);
    }
  };

  // iOS Safari specific: ensure title is explicitly set
  const notificationTitle = data.title || "O'KEY ENGLISH";
  console.log('[SW] Showing notification with title:', notificationTitle, 'body:', data.body?.substring(0, 50));

  event.waitUntil(
    Promise.all([
      self.registration.showNotification(notificationTitle, options),
      broadcastToClients(),
    ])
  );
});

// ============= NOTIFICATION CLICK =============
// Types for notification data
interface NotificationPayloadData {
  url?: string;
  [key: string]: unknown;
}

self.addEventListener('notificationclick', (event) => {
  console.log('[SW] Notification clicked:', event.action);
  
  event.notification.close();

  if (event.action === 'close') {
    return;
  }

  const notificationData = event.notification.data as NotificationPayloadData | undefined;
  const urlToOpen = notificationData?.url || '/';

  if (event.action === 'close') {
    return;
  }

  event.waitUntil(
    self.clients.matchAll({ type: 'window', includeUncontrolled: true }).then((clientList) => {
      // Check if there's already a window open
      for (const client of clientList) {
        if (client.url.includes(self.location.origin) && 'focus' in client) {
          client.focus();
          if (urlToOpen !== '/') {
            (client as WindowClient).navigate(urlToOpen);
          }
          return;
        }
      }
      // Open new window
      return self.clients.openWindow(urlToOpen);
    })
  );
});

